# Type function and typecasting

a = 10
b = 3.14
c = "10.8"
name = "Taran"

# typecasting
d = float(a)        # int → float
e = float(c)        # string → float

# checking types
print(type(a))      # int
print(type(b))      # float
print(type(c))      # str
print(type(name))   # str
print(type(d))      # float
print(type(e))      # float

# printing values also
print(a, b, c, name, d, e) 